void    ft_push_b(int *a, int *i, int *b, int *j)
{
    if (*i >= 0) 
    {
        int k;

        k = *i;
        while(k > 0)
        {
            a[k] = a[k - 1];
            k--;
        }
        b[++*j] = a[*i];
        *i -= 1;
    }
}